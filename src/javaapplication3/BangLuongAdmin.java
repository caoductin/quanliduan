    public void show_BangNhanVienLamViec(){
         ResultSet rs ;
         PreparedStatement ps;
         Statement st;
         int count = 0;
      
         Connection con = myConnection.getConnection();
        try {
            st = con.createStatement();
            String query1 = "SELECT Nhanvien.MaNV, Nhanvien.Hoten,\n" +
"  COUNT(CASE WHEN PhanCong.TrangThai = 'hoan thanh' THEN 1 END) AS SoHoanThanh,\n" +
"  COUNT(CASE WHEN PhanCong.TrangThai = 'chua hoan thanh' THEN 1 END) AS SoChuaHoanThanh,\n" +
"  COUNT(CASE WHEN PhanCong.TrangThai = 'khong hoan thanh' THEN 1 END) AS SoKhongHoanThanh,\n" +
"  COUNT(CASE WHEN PhanCong.TrangThai = 'hoan thanh' THEN 1 END) * "+ this.getLuong() +" AS luong\n" +
"FROM Nhanvien\n" +
"LEFT JOIN PhanCong ON Nhanvien.MaNV = PhanCong.MaNV\n" +
"WHERE MONTH(PhanCong.NgayLam) = MONTH(CURDATE())\n" +
"AND YEAR(PhanCong.NgayLam) = YEAR(CURDATE())\n" +
"GROUP BY Nhanvien.MaNV";
            rs = st.executeQuery(query1);
               DefaultTableModel model =(DefaultTableModel)jTableBangTheoDoiNV.getModel();
            while(rs.next()){
                    
                 count++;
                 
                 Object[] rowData = {rs.getString("MaNV"), rs.getString("Hoten"),rs.getString("SoHoanThanh"),rs.getString("SoChuaHoanThanh")
                 ,rs.getString("SoKhongHoanThanh"),rs.getString("luong")};
                 model.addRow(rowData);
               
                    
                        
              }
        //  model.setRowCount(count);
                
                
                
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(BangChonNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
   
        
    }
    
    
